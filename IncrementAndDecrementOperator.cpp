
#include<iostream>
using namespace std;

int main(){

    int n=0;

    cout<< n++ <<endl;  // ++ is used after variable it is called post increment.
                        // n++ means n = n+1
    cout<< n <<endl;

    cout << ++n <<endl; // ++ is used before variable it is called pre increment.
                        // ++n means n = n+1
    cout<<n<<endl;

    cout<< --n<<endl;
    cout<< n-- <<endl;
    cout<< n <<endl;




return 0;
}

